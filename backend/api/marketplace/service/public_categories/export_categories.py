result = []
id_counter = 1

def flatten_categories(categories, parent_id=None):
    global id_counter
    for cat in categories:
        current_id = id_counter
        id_counter += 1
        result.append({
            "id": current_id,
            "name": cat["title"],
            "parent_id": parent_id,
            "external_id": cat["value"],
            "description": cat.get("description"),
            "is_active": True
        })
        if "children" in cat:
            flatten_categories(cat["children"], current_id)

