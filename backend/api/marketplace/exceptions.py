"""
Исключения для модуля маркетплейса.
Определяет специфичные исключения для различных сценариев в маркетплейсе.
"""


class MarketplaceError(Exception):
    """
    Базовое исключение для всех ошибок маркетплейса.
    Наследуется всеми специфичными исключениями модуля.
    """
    pass


class NotPublicError(MarketplaceError):
    """
    Исключение для случаев, когда товар или локация не являются публичными.
    
    Сценарии использования:
    - Попытка получить товар с public=False
    - Попытка получить локацию с public=False
    - Попытка создать заказ на непубличный товар
    - Попытка оставить отзыв на непубличную локацию
    """
    pass


class NotFoundError(MarketplaceError):
    """
    Исключение для случаев, когда запрашиваемая сущность не найдена.
    
    Сценарии использования:
    - Товар с указанным ID не существует
    - Локация с указанным ID не существует
    - QR-код не найден в базе данных
    - Заказ с указанным ID не найден
    - Отзыв с указанным ID не найден
    - Элемент избранного не найден
    """
    pass


class AlreadyInFavoritesError(MarketplaceError):
    """
    Исключение для случаев, когда пользователь пытается добавить 
    уже существующий в избранном элемент.
    
    Сценарии использования:
    - Попытка добавить товар, который уже в избранном пользователя
    - Попытка добавить локацию, которая уже в избранном пользователя
    """
    pass


class ValidationError(MarketplaceError):
    """
    Исключение для ошибок валидации данных.
    
    Сценарии использования:
    - Некорректный рейтинг (не в диапазоне 1-5)
    - Слишком короткий или длинный текст отзыва
    - Некорректный тип сущности для избранного
    - Некорректные параметры запроса
    - Некорректные данные заказа
    """
    pass


